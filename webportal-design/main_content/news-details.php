                        <p>
                            مارس 2019 ، وقعت الشركة الوطنية الأولى للموارد البشرية (FNRCO) ، وهي شركة متخصصة في خدمات الموارد البشرية وحلول القوى العاملة في المملكة العربية السعودية ، شراكة إستراتيجية مع شركة Archetype Foundry Consultancy ، وهي عبارة عن مركز للأبحاث والتصميم والتطوير تم إنشاؤه عام 2014 من قبل ثلاثة من الشباب المحترفين والأكفاء. وتختص الشركة بتصميم وبناء النماذج الأولية وحلول المنتجات في مختلف المجالات باستخدام هندسة الأجهزة الأكثر تطوراً وأحدث الأساليب في تقنيات "Pronto" وتقنيات التصنيع الرقمي.




                        </p>
                        <p>

                            وقد وقع عقد الشراكة كل من علي جابر المهان، المدير العام لشركة الوطنية الأولى FNRCO ، والدكتور سلمان عبد الله العريبي، الشريك المؤسس لشركة  Archetype Foundry Consultancy. تهدف هذه الشراكة إلى وضع نهج تصميمي للموارد البشرية من خلال انشاء منصة تعاونية مفتوحة تسخر المواهب المحلية والإقليمية والدولية التي سيتم استضافتها ووجودها لدى الشركتين سواء في المملكة العربية السعودية ودولة البحرين.



                        </p>
 <p>
     تتمتع FNRCO بخبرة كبيرة في مجال تقديم خدمات تطوير الأعمال والمشاريع في المملكة العربية السعودية. كما أنها شركة مسجلة ومرخصة من قبل وزارة العمل السعودية، كما تقدم خدمات التدريب لجميع التخصصات المهنية والإدارية بموجب تصريح من المؤسسة العامة للتعليم الفني والتدريب المهني.



 </p>

                        <p>
                            ويشمل التدريب خدمات الصيانة والتفتيش للأعمال الكهربائية والميكانيكية والإلكترونية للشركات الصناعية والبترولية والبتروكيماوية. وتعمل FNRCO بالتعاون مع صندوق تنمية الموارد البشرية ووزارة العمل والتنمية الاجتماعية السعودية لدعم الرؤية الوطنية والعمل على زيادة توظيف المواطنين السعوديين.


